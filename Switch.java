class Switch
{
    public static void main(String[] args)
    {
        int n = 1;
        switch(n)
        {
            case 1: 
        
                System.out.println("yes");
                break;
            case 2:
                System.out.println("no");
                break;
            default:
                System.out.println("enter valid number");

        }
    }
}