class literals{
    public static void main(String[] agrs){
        //literals
        int a = 0x71e; //in hexa decimal values we can mention it a 'x'
        System.out.println(a);

        int b = 10_00_00; //we can give underscore b/w numbers to avoid confusion
        System.out.println(b);

        double c = 50;
        System.out.println(c);

        char d = 'k'; //single letter should be decleared in single codes
        String e = "Vishnu";
        System.out.println(d + e);
    }   
}